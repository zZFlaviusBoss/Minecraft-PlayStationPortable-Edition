static inline bool isBaseStairId(uint8_t id) {
  return id == BLOCK_STONE_STAIR || id == BLOCK_WOOD_STAIR || id == BLOCK_COBBLE_STAIR ||
         id == BLOCK_SANDSTONE_STAIR || id == BLOCK_BRICK_STAIR || id == BLOCK_STONE_BRICK_STAIR;
}

static inline bool isStairId(uint8_t id) {
  return isBaseStairId(id) ||
         (id >= BLOCK_WOOD_STAIR_EAST && id <= BLOCK_STONE_BRICK_STAIR_WEST) ||
         (id >= BLOCK_WOOD_STAIR_TOP && id <= BLOCK_STONE_BRICK_STAIR_TOP_WEST);
}

