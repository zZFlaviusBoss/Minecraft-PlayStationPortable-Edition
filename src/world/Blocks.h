    case BLOCK_WOOD_STAIR_TOP_SOUTH:
    case BLOCK_COBBLE_STAIR_TOP_SOUTH:
    case BLOCK_STONE_STAIR_TOP_SOUTH:
    case BLOCK_SANDSTONE_STAIR_TOP_SOUTH:
    case BLOCK_BRICK_STAIR_TOP_SOUTH:
    case BLOCK_STONE_BRICK_STAIR_TOP_SOUTH: return 2;
    case BLOCK_WOOD_STAIR_WEST:
    case BLOCK_COBBLE_STAIR_WEST:
    case BLOCK_STONE_STAIR_WEST:
