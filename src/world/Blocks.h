    case BLOCK_STONE_STAIR_EAST:
    case BLOCK_SANDSTONE_STAIR_EAST:
    case BLOCK_BRICK_STAIR_EAST:
    case BLOCK_STONE_BRICK_STAIR_EAST: return 1;
    case BLOCK_WOOD_STAIR_TOP_EAST:
    case BLOCK_COBBLE_STAIR_TOP_EAST:
    case BLOCK_STONE_STAIR_TOP_EAST:
    case BLOCK_SANDSTONE_STAIR_TOP_EAST:
    case BLOCK_BRICK_STAIR_TOP_EAST:
    case BLOCK_STONE_BRICK_STAIR_TOP_EAST: return 1;
    case BLOCK_WOOD_STAIR_SOUTH:
    case BLOCK_COBBLE_STAIR_SOUTH:
    case BLOCK_STONE_STAIR_SOUTH:
