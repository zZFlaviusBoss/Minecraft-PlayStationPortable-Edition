static inline uint8_t toOrientedStairId(uint8_t baseId, int facing, bool upsideDown = false) {
  int f = facing & 3;
  if (!upsideDown && f == 0) return stairBaseId(baseId);
  switch (stairBaseId(baseId)) {
    case BLOCK_WOOD_STAIR:
      if (upsideDown) return (f == 0) ? BLOCK_WOOD_STAIR_TOP : (f == 1) ? BLOCK_WOOD_STAIR_TOP_EAST : (f == 2) ? BLOCK_WOOD_STAIR_TOP_SOUTH : BLOCK_WOOD_STAIR_TOP_WEST;
      return (f == 1) ? BLOCK_WOOD_STAIR_EAST : (f == 2) ? BLOCK_WOOD_STAIR_SOUTH : BLOCK_WOOD_STAIR_WEST;
    case BLOCK_COBBLE_STAIR:
      if (upsideDown) return (f == 0) ? BLOCK_COBBLE_STAIR_TOP : (f == 1) ? BLOCK_COBBLE_STAIR_TOP_EAST : (f == 2) ? BLOCK_COBBLE_STAIR_TOP_SOUTH : BLOCK_COBBLE_STAIR_TOP_WEST;
      return (f == 1) ? BLOCK_COBBLE_STAIR_EAST : (f == 2) ? BLOCK_COBBLE_STAIR_SOUTH : BLOCK_COBBLE_STAIR_WEST;
    case BLOCK_STONE_STAIR:
      if (upsideDown) return (f == 0) ? BLOCK_STONE_STAIR_TOP : (f == 1) ? BLOCK_STONE_STAIR_TOP_EAST : (f == 2) ? BLOCK_STONE_STAIR_TOP_SOUTH : BLOCK_STONE_STAIR_TOP_WEST;
      return (f == 1) ? BLOCK_STONE_STAIR_EAST : (f == 2) ? BLOCK_STONE_STAIR_SOUTH : BLOCK_STONE_STAIR_WEST;
    case BLOCK_SANDSTONE_STAIR:
      if (upsideDown) return (f == 0) ? BLOCK_SANDSTONE_STAIR_TOP : (f == 1) ? BLOCK_SANDSTONE_STAIR_TOP_EAST : (f == 2) ? BLOCK_SANDSTONE_STAIR_TOP_SOUTH : BLOCK_SANDSTONE_STAIR_TOP_WEST;
      return (f == 1) ? BLOCK_SANDSTONE_STAIR_EAST : (f == 2) ? BLOCK_SANDSTONE_STAIR_SOUTH : BLOCK_SANDSTONE_STAIR_WEST;
    case BLOCK_BRICK_STAIR:
      if (upsideDown) return (f == 0) ? BLOCK_BRICK_STAIR_TOP : (f == 1) ? BLOCK_BRICK_STAIR_TOP_EAST : (f == 2) ? BLOCK_BRICK_STAIR_TOP_SOUTH : BLOCK_BRICK_STAIR_TOP_WEST;
      return (f == 1) ? BLOCK_BRICK_STAIR_EAST : (f == 2) ? BLOCK_BRICK_STAIR_SOUTH : BLOCK_BRICK_STAIR_WEST;
    case BLOCK_STONE_BRICK_STAIR:
      if (upsideDown) return (f == 0) ? BLOCK_STONE_BRICK_STAIR_TOP : (f == 1) ? BLOCK_STONE_BRICK_STAIR_TOP_EAST : (f == 2) ? BLOCK_STONE_BRICK_STAIR_TOP_SOUTH : BLOCK_STONE_BRICK_STAIR_TOP_WEST;
      return (f == 1) ? BLOCK_STONE_BRICK_STAIR_EAST : (f == 2) ? BLOCK_STONE_BRICK_STAIR_SOUTH : BLOCK_STONE_BRICK_STAIR_WEST;
    default: return baseId;
  }
}
